class Solution {
    public boolean isHappy(int n) {

        Set<String> nums=new HashSet<>();
        String num=n+"";
        while (!num.equals("1") && !nums.contains(num)){
            System.out.println("num:"+num);
            nums.add(num);
            int sum=0;
            for (int i=0;i<num.length();i++){
                int e=num.charAt(i)-'0';
                sum=sum+e*e;
            }
            num=sum+"";
        }
        if (num.equals("1")) return true;
        return false;
    }
}
