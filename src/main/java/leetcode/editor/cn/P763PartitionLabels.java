    public List<Integer> partitionLabels(String S) {
        List<Integer> res=new ArrayList<>();
        int[] next=new int[26];
        for (int i=0;i<S.length();i++){
            char ch=S.charAt(i);
            next[ch-'a']=i;
        }
        int start=0;
        int end=next[S.charAt(0)-'a'];
        for (int  i=start;i<S.length();i++){
            end=Math.max(end,next[S.charAt(i)-'a']);
            if (i==end){
                res.add(end-start+1);
                start=end+1;
            }
        }
        return res;
    }
